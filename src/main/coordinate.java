package main;

public class coordinate {
	private float x, y;

	public coordinate(float x2, float y2) {
		this.x = x2;
		this.y = y2;
	}

	public float getX() {
		return x;
	}

	public float getY() {
		return y;
	}

	public void setX(float x) {
		this.x = x;
	}

	public void setY(float y) {
		this.y = y;
	}
}
